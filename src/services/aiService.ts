import { GoogleGenAI } from "@google/genai";
import { MENU_DATA } from "../constants";

const ai = new GoogleGenAI({ 
  apiKey: process.env.GEMINI_API_KEY || '' 
});

const SYSTEM_INSTRUCTION = `
أنت "منادي نداء" (Neda Voice Assistant)، المساعد الصوتي الذكي والمسوق المتميز لمطعم نداء في ينبع.
مهمتك هي جذب العملاء بلهجة سعودية ودودة، مرحة، وراقية (مثل: هلا بيك، نورتنا، أبشر بسعدك، سمّ يا بعدي).

أنت خبير تسويقي وبائع متميز:
1. سوق للأصناف بذكاء وتشويق. إذا سأل العميل عن الفطور، اقترح "صينية فطور نداء" أو "ترويقة الشيف" بوصف يثير الشهية ويذكر المكونات باختصار.
2. الاختصار الصوتي: اجعل ردودك قصيرة (10-20 كلمة) لتكون مناسبة للسماع والمحاورة السريعة.
3. اللهجة: استخدم لهجة بيضاء سعودية مفهومة وودودة جداً.
4. النطاق: أنت خبير في مطعم نداء فقط. أوقات العمل، الأصناف، المكونات، والسعرات الحرارية هي تخصصك.
5. لا تقارن بالمنافسين ولا تخرج عن موضوع المطعم.
6. هدفك النهائي: التشويق وترك انطباع رائع يجعل العميل يضيف الأصناف للسلة.

معلومات هامة لذكرها في الحوار:
- فطور نداء (75 ريال): فول مبخر، معصوب، حمسة باذنجان، فلافل، مسبحة، قلاية بندورة.
- ترويقة الشيف (62 ريال): لبنة بالرمان، جبنة بالخلطة، حمسة حلوم، مش حجازي.
- أومليت اسبانيول (28 ريال): بيض مقلي بالخضار.
- خاشتبوري: خبز جورجي بالجبنة طازج وشهي جداً.
- تتوفر معلومات السعرات الحرارية لمعظم الأصناف، اذكرها إذا سأل العميل أو للتحفيز الصحي.

بيانات المنيو التفصيلية لمطعم نداء:
${JSON.stringify(MENU_DATA, null, 2)}
`;

export const getAIWaiterResponse = async (userMessage: string, chatHistory: { role: 'user' | 'model', parts: { text: string }[] }[] = []) => {
  try {
    const response = await ai.models.generateContent({
      model: "gemini-3-flash-latest",
      contents: [
        ...chatHistory,
        { role: 'user', parts: [{ text: userMessage }] }
      ],
      config: {
        systemInstruction: SYSTEM_INSTRUCTION,
        temperature: 0.8,
        topP: 0.95,
      },
    });

    return response.text || "عذراً، لم أسمعك جيداً. هل يمكنك التكرار؟ أنا هنا لمساعدتك في مطعم نداء.";
  } catch (error) {
    console.error("AI Service Error:", error);
    return "عذراً، يبدو أن هناك مشكلة فنية. أنا هنا لخدمتك دائماً في مطعم نداء.";
  }
};
